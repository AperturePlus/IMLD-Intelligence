                                        created_at              TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS file_asset (
                            id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                            tenant_id               BIGINT NOT NULL REFERENCES tenant(id),
                            biz_type                VARCHAR(32) NOT NULL,             -- GENETIC_REPORT/REPORT_SIGN/IMAGING_REPORT/ATTACHMENT
                            file_name               VARCHAR(255) NOT NULL,
                            file_ext                VARCHAR(16),
                            mime_type               VARCHAR(100),
                            file_size               BIGINT,
                            storage_provider        VARCHAR(32) NOT NULL,             -- MINIO/OSS/NAS
                            storage_key             VARCHAR(500) NOT NULL,
                            sha256                  VARCHAR(64),
                            uploaded_by             BIGINT REFERENCES user_account(id),
                            uploaded_at             TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS genetic_report (
                                id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                tenant_id               BIGINT NOT NULL REFERENCES tenant(id),
                                patient_id              BIGINT NOT NULL REFERENCES patient(id),
                                encounter_id            BIGINT REFERENCES encounter(id),
                                report_source           VARCHAR(200),                     -- 检测机构
                                report_date             DATE,
                                file_id                 BIGINT REFERENCES file_asset(id),
                                parse_status            VARCHAR(32) NOT NULL DEFAULT 'PENDING', -- PENDING/PARSED/FAILED/MANUAL
                                summary                 TEXT,
                                conclusion              VARCHAR(64),                      -- POSITIVE/NEGATIVE/UNCERTAIN
                                created_at              TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS genetic_variant (
                                 id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                 tenant_id               BIGINT NOT NULL REFERENCES tenant(id),
                                 report_id               BIGINT NOT NULL REFERENCES genetic_report(id),
                                 gene                    VARCHAR(64) NOT NULL,
                                 chromosome              VARCHAR(16),
                                 position                BIGINT,
                                 ref_allele              VARCHAR(32),
                                 alt_allele              VARCHAR(32),
                                 variant_type            VARCHAR(32),                      -- SNV/INDEL/CNV
                                 zygosity                VARCHAR(32),                      -- HOM/HET/COMPOUND_HET
                                 classification          VARCHAR(64),                      -- PATHOGENIC/LIKELY_PATHOGENIC/VUS/BENIGN
                                 hgvs_c                  VARCHAR(255),
                                 hgvs_p                  VARCHAR(255),
                                 evidence                TEXT,
                                 source_type             VARCHAR(32) NOT NULL DEFAULT 'PARSED',
                                 created_at              TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS imaging_report (
                                id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                tenant_id               BIGINT NOT NULL REFERENCES tenant(id),
                                patient_id              BIGINT NOT NULL REFERENCES patient(id),
                                encounter_id            BIGINT REFERENCES encounter(id),
                                modality                VARCHAR(32),                      -- US/CT/MRI/PATHOLOGY
                                report_text             TEXT NOT NULL,                    -- 仅存结构化文字报告
                                file_id                 BIGINT REFERENCES file_asset(id), -- 报告附件（PDF等）
                                source_system           VARCHAR(32) NOT NULL DEFAULT 'PACS',
                                examined_at             TIMESTAMPTZ,
                                created_at              TIMESTAMPTZ NOT NULL DEFAULT now()
);